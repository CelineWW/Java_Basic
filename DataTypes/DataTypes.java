public class DataTypes
{
    public static void main(String[] args) {
        System.out.print("integer(3):  ");
        System.out.println(3);
        System.out.print("long(3L):  ");
        System.out.println(3L);
        System.out.print("float(1.234F):  ");
        System.out.println(1.234F);
        System.out.print("double(1.234):  ");
        System.out.println(1.234);
        System.out.print("Character(#):  ");
        System.out.println('#');
        System.out.print("Boolean(true):  ");
        System.out.println(true);
        System.out.print("Binary(0b0101010):  ");
        System.out.println(0b0101010);
        System.out.print("Hexadecimal(0x13BD):  ");
        System.out.println(0x13BD);
        System.out.print("Strings(Hello):  ");
        System.out.println("Hello");
    }// End main
} // End DataTypes
